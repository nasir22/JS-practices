const month = "march"

switch (month) {
    case "jan":
        console.log("January");
        
        break;
    case "Feb":
        console.log("Feb");
        
        break;
    case "march":
        console.log("March");
        
        break;
    case "April":
        console.log("April");
        
        break;

    default:
        console.log("the month did not matched ")
        break;
}

// the break statement is used to written so that if any statement got true then the remainin statements wont get printed it breaks the control 